CREATE TABLE footwear(
	Order_ID INT PRIMARY KEY,
	Order_Date DATE,
	Customer_Name VARCHAR(50),
	City CHAR(30),
	State CHAR(15),
	Region CHAR(6),
	Country CHAR (15),
	Category VARCHAR(30),
	Sub_Category CHAR(30),
	Product_Name CHAR(30),
	Quantity INT,
	Unit_Price FLOAT,
	Revenue FLOAT,
	Profit FLOAT
);
